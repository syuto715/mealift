/** Maps a birth year to an AgeRange bucket. The current year is
 *  optional for deterministic testing. */
export function ageRangeFromBirthYear(
  birthYear: number,
  now: Date = new Date(),
): AgeRange {
  const age = now.getFullYear() - birthYear;
  if (age < 10) return 'under-10';
  if (age < 15) return '10-14';
  if (age < 20) return '15-19';
  if (age < 25) return '20-24';
  if (age < 30) return '25-29';
  if (age < 35) return '30-34';
  if (age < 40) return '35-39';
  if (age < 45) return '40-44';
  if (age < 50) return '45-49';
  if (age < 55) return '50-54';
  if (age < 60) return '55-59';
  if (age < 65) return '60-64';
  if (age < 70) return '65-69';
  if (age < 75) return '70-74';
  if (age < 80) return '75-79';
  if (age < 85) return '80-84';
  return '85-plus';
}

/** Type guard that `LLMClient.chat()`'s return value owns the
 *  side-channels we documented. Useful in tests that mock partial
 *  implementations. */
export function isChatStream(value: unknown): value is ChatStream {
  return (
    typeof value === 'object' &&
    value !== null &&
    Symbol.asyncIterator in value &&
    'meta' in value &&
    'done' in value
  );
}

